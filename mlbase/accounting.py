def get_mar_levels(gl_number, max_mar_levels = 4):
    mar = mar_lkp
    mar_levels = []
    level = 1
    if gl_number is None or len(gl_number)==0 or gl_number[0] not in ["1","2","3","4","5","6","7"]:
        return ["x"*(i+1) for i in range(4)]
    while len(mar_levels) < max_mar_levels:
        mar_code = gl_number[:level]
        if mar_code in mar:
            mar_levels.append(mar_code)
            mar = mar[mar_code]
            level +=1
        else:
            # leaf node reached
            while len(mar_levels) < max_mar_levels:
                mar_levels.append(mar_levels[-1]+"x")
    return mar_levels
